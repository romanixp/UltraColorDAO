package dao.to;

/**
 *
 * @author Sistemas
 */
public class ComprobanteTO {
    private String codigo;
    private String ventCodigo;
    private String compCodigo;
    private String nombre;
    private String numero;
    private String noSerie;

    public String getCodigo() {
        return codigo;
    }

    public void setCodigo(String codigo) {
        this.codigo = codigo;
    }

    public String getVentCodigo() {
        return ventCodigo;
    }

    public void setVentCodigo(String ventCodigo) {
        this.ventCodigo = ventCodigo;
    }

    public String getCompCodigo() {
        return compCodigo;
    }

    public void setCompCodigo(String compCodigo) {
        this.compCodigo = compCodigo;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getNumero() {
        return numero;
    }

    public void setNumero(String numero) {
        this.numero = numero;
    }

    public String getNoSerie() {
        return noSerie;
    }

    public void setNoSerie(String noSerie) {
        this.noSerie = noSerie;
    }

    
    
}
